#include <stdio.h>
#include <stdlib.h>
#include <math.h>
/**
 * add - function that adds 2 integers
 * @a: first argument
 * @b: second argument
 * Return: an integer
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function that subtracts b from a
 * @a: first integer
 * @b: second integer
 * Return: an integer
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function that returns multiplication
 * @a: the 1st integer
 * @b: the second integer
 * Return: integer
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function that divides a into b
 * @a: the first integer
 * @b: the second integer
 * Return: an integer
 */
int my_div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function that retrns modulus of 2 integers
 * @a: the first integer
 * @b: the second integer
 * Return: an integer
 */
int mod(int a, int b)
{
	return (a % b);
}
